// Escape Sequences

#include <stdio.h>
int main()
{
    printf("Welcome User\n");
    printf("Welcome\t User\n");
    printf("Welcome\b User\n");
    printf("Welcome\r User\n");
    printf("Welcome User, How are You\n");
    printf("\vI am Fine\n");
    printf("\"Welcome User\"\n");
    printf("Welcome\\User\n");

    return 0;
}